        OLED_SetCursor(0U, 16U);
        snprintf(line, sizeof(line), "BEEP: %s", HAL_GPIO_ReadPin(APP_BUZZER_GPIO_PORT, APP_BUZZER_GPIO_PIN) == GPIO_PIN_SET ? "ON" : "OFF");
